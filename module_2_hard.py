# Входное число для первой вставки
n = int(input("Введите число от 3 до 20: "))

# Переменная для хранения результата
result = ""

# Перебор всех возможных чисел от 1 до n-1 для создания уникальных пар
for i in range(1, n):
    for j in range(i + 1, n):
        # Проверяем, делится ли n на сумму пары i и j
        if n % (i + j) == 0:
            # Добавляем пару к результату в виде строки
            result += f"{i}{j}"

# Вывод результата
print(result)
