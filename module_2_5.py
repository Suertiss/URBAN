def get_matrix(n, m, value):
    # Если n или m меньше или равно нулю, возвращаем пустой список
    if n <= 0 or m <= 0:
        return []

    # Инициализация пустого списка для матрицы
    matrix = []

    # Заполнение матрицы
    for i in range(n):  # Внешний цикл для строк
        row = []  # Создаём пустую строку
        for j in range(m):  # Внутренний цикл для столбцов
            row.append(value)  # Добавляем значение value в строку
        matrix.append(row)  # Добавляем строку в матрицу

    return matrix  # Возвращаем готовую матрицу
